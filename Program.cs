using CustomersAPI.Data;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
////////////// Add new Value ///////////////
//builder.Services.AddDbContext<CustomersAPIDbContext>(options => options.UseInMemoryDatabase("CustomerDb"));

builder.Services.AddDbContext<CustomersAPIDbContext>(options =>
options.UseSqlServer(builder.Configuration.GetConnectionString("CustomerApiConnectionString")));



var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
